定理：正整数x,y,z构成一个本原毕达哥拉斯三元组且y为偶数，当且仅当存在互素的正整数m，n(m>n),其中m为奇数n为偶数，或者m为偶数n为奇数，并且满足：
                                           x=m^2-n^2
