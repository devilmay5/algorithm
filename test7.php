 * */


for($i=1;$i<1000;$i=$i+2){
    for($j=2;$j<1000;$j=$j+2){

        $x = pow($j,2) - pow($i,2);
        $y = 2*$j*$i;
        $z =  pow($j,2) + pow($i,2);

        if(((pow($x,2)+pow($y,2))==pow($z,2))&&($x+$y+$z==1000)){

               echo  $x+$y+$z."====".$x."~~".$y."~~".$z."\r\n";
               echo   (pow($x,2)+pow($y,2)).'=='.pow($z,2)."\r\n";
               echo $x*$y*$z;
               exit;
        }
    }
